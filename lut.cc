static double sRGB_to_linear(double c)
{
  return (c <= 0.04045) ? (c / 12.92) : pow((c + 0.055) / 1.055, 2.4);
}

// sRGB [0,255] to CIELCH chroma
double compute_chroma(const uint8_t r, const uint8_t g, const uint8_t b)
{
  const double R{sRGB_to_linear(r / 255.0)};
  const double G{sRGB_to_linear(g / 255.0)};
  const double B{sRGB_to_linear(b / 255.0)};

  constexpr double Xn{0.95047};
  constexpr double Yn{1.0};
  constexpr double Zn{1.08883};

  const double X = R * 0.4124564 / Xn + G * 0.3575761 / Xn + B * 0.1804375 / Xn;
  const double Y = R * 0.2126729 / Yn + G * 0.7151522 / Yn + B * 0.0721750 / Yn;
  const double Z = R * 0.0193339 / Xn + G * 0.1191920 / Xn + B * 0.9503041 / Zn;

  auto f{[](double t) {
    return (t > 0.008856) ? std::cbrt(t) : (7.787 * t + 16.0 / 116.0);
  }};

  const double fx{f(X)};
  const double fy{f(Y)};
  const double fz{f(Z)};

  double a{500.0 * (fx - fy)};
  double b2{200.0 * (fy - fz)};
  double chroma{std::sqrt(a * a + b2 * b2)};
  return chroma;
}

static inline float f_xyz(const float v)
{
  constexpr float epsilon{0.008856f};
  constexpr float kappa{7.787f};
  constexpr float delta{16.0f / 116.0f};
  return (v > epsilon) ? std::cbrt(v) : (kappa * v + delta);
}

